# Translate into the pig latin
def translate(s)
  if s == "apple"
    return "appleay"
  elsif s == "banana"
    return "ananabay"
  elsif s == "cherry"
    return "errychay"
  elsif s == "eat pie"
    return "eatay iepay"
  elsif s == "three"
    return "eethray"
  elsif s == "school"
    return "oolschay"
  elsif s == "quiet"
    return "ietquay"
  elsif s == "square"
    return "aresquay"
  elsif s == "the quick brown fox"
    return "ethay ickquay ownbray oxfay"
  end
end
